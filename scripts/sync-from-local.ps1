function Export-Skills {
  $skillsSource = Join-Path $CodexRoot "skills"
  Remove-GeneratedFolder "skills/frontend"
  Remove-GeneratedFolder "skills/backend-database"
  Remove-GeneratedFolder "skills/project-manager"
  Remove-GeneratedFolder "skills/existing-global"

  if (-not (Test-Path -LiteralPath $skillsSource)) {
    $script:Summary.warnings += "Missing skills source: $skillsSource"
    return
  }

  Get-ChildItem -LiteralPath $skillsSource -Directory -Force | ForEach-Object {
    $skillName = $_.Name
    $category = Get-SkillCategory $skillName
    $dest = Join-Path $RepoRoot ("skills/$category/$skillName")
    Ensure-Dir $dest

    Copy-IfExists (Join-Path $_.FullName "SKILL.md") (Join-Path $dest "SKILL.md")
    Get-ChildItem -LiteralPath $_.FullName -Recurse -File -Force -ErrorAction SilentlyContinue | Where-Object {
      $name = $_.Name
      if ($_.FullName -match "\\node_modules\\|\\.git\\|\\.understand-anything\\|\\.retrieval\\|\\.codegraph\\|\\.ai-task\\") { return $false }
      if ($name -eq "SKILL.md") { return $false }
      return ($_.Extension -in @(".md",".mjs",".js",".sh") -or $name -eq "package.json")
    } | ForEach-Object {
      $relative = Get-SafeRelativePath (Join-Path $skillsSource $skillName) $_.FullName
      Copy-IfExists $_.FullName (Join-Path $dest $relative)
    }
  }
}

