function Ensure-Dir {
  param([string]$Path)
  if (-not (Test-Path -LiteralPath $Path)) {
    New-Item -ItemType Directory -Force -Path $Path | Out-Null
  }
}

function Get-SafeRelativePath {
  param([string]$BasePath, [string]$FullPath)
  $base = (Resolve-Path -LiteralPath $BasePath).Path.TrimEnd("\")
  $full = (Resolve-Path -LiteralPath $FullPath).Path
  if ($full.StartsWith($base, [System.StringComparison]::OrdinalIgnoreCase)) {
    return $full.Substring($base.Length).TrimStart("\")
  }
  return $full
}

function Remove-GeneratedFolder {
  param([string]$RelativePath)
  $target = Join-Path $RepoRoot $RelativePath
  if (Test-Path -LiteralPath $target) {
    Remove-Item -LiteralPath $target -Recurse -Force
  }
  Ensure-Dir $target
}

function Copy-IfExists {
  param([string]$Source, [string]$Destination)
  if (Test-Path -LiteralPath $Source -PathType Leaf) {
    Ensure-Dir (Split-Path -Parent $Destination)
    Copy-Item -LiteralPath $Source -Destination $Destination -Force
    $script:Summary.filesCopied++
  }
}

function Copy-Pattern {
  param([string]$SourceRoot, [string]$DestinationRoot, [string]$RelativePattern)
  $base = Split-Path -Parent $RelativePattern
  $leaf = Split-Path -Leaf $RelativePattern
  $sourceDir = if ($base -and $base -ne ".") { Join-Path $SourceRoot $base } else { $SourceRoot }
  if (-not (Test-Path -LiteralPath $sourceDir)) { return }
  Get-ChildItem -LiteralPath $sourceDir -Filter $leaf -File -Force -ErrorAction SilentlyContinue | ForEach-Object {
    $relative = Get-SafeRelativePath $SourceRoot $_.FullName
    Copy-IfExists $_.FullName (Join-Path $DestinationRoot $relative)
  }
}

